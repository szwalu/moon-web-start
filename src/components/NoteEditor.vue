const minEditorHeight = 130
const isSmallScreen = window.innerWidth < 768
let maxEditorHeight
if (isSmallScreen)
  maxEditorHeight = window.innerHeight * 0.65
else
  maxEditorHeight = Math.min(window.innerHeight * 0.75, 800)

